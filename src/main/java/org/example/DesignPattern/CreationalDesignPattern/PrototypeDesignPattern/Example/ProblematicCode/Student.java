package org.example.DesignPattern.CreationalDesignPattern.PrototypeDesignPattern.Example.ProblematicCode;

public class Student {
    private int roll;
    public int age;
    public String name;

    Student(){}

    public Student(int roll, int age, String name){
        this.roll = roll;
        this.age = age;
        this.name = name;
    }
}
